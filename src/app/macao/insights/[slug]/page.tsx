function parseLang(raw?: string): Lang {
  if (raw && VALID_LANGS.includes(raw as Lang)) return raw as Lang
  return 'zh'
}

async function getInsight(slug: string, lang: Lang) {
  const { data } = await supabase
    .from('insights')
    .select('*')
    .eq('slug', slug)
    .eq('lang', lang)
    .eq('status', 'published')
    .single()
  return data as InsightArticle | null
}

async function getAvailableLangs(slug: string): Promise<Lang[]> {
  const { data } = await supabase
    .from('insights')
    .select('lang')
    .eq('slug', slug)
    .eq('status', 'published')
  if (!data) return []
  return data.map(d => d.lang as Lang).filter(l => VALID_LANGS.includes(l))
}

interface RelatedMerchant {
  slug: string
  name_zh: string
  name_en?: string
  district?: string
  google_rating?: number
  website?: string | null
  category: { slug: string; name_zh: string; icon?: string } | null
}

async function getRelatedMerchants(slugs: string[]): Promise<RelatedMerchant[]> {
  const validSlugs = (slugs || []).filter(s => s && typeof s === 'string' && s !== 'null')
  if (!validSlugs.length) return []
  const { data } = await supabase
    .from('merchants')
    .select('slug, name_zh, name_en, category:categories(slug, name_zh, icon), district, google_rating, website')
    .in('slug', validSlugs)
    .eq('status', 'live')
  if (!data) return []
  return data.map((d: Record<string, unknown>) => ({
    ...d,
    category: Array.isArray(d.category) ? d.category[0] || null : d.category,
  })) as RelatedMerchant[]
}

