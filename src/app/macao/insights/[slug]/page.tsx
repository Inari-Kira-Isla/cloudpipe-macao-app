// Fallback: fetch top-rated merchants from related industries when no slugs assigned
async function getFallbackMerchants(industries: string[]): Promise<RelatedMerchant[]> {
  if (!industries?.length) return []
  const categorySlugs = industries.flatMap(ind => INDUSTRY_TO_CATEGORIES[ind] || [])
  if (!categorySlugs.length) return []
  const { data: cats } = await supabase
    .from('categories')
    .select('id')
    .in('slug', categorySlugs)
  const catIds = (cats || []).map((c: { id: string }) => c.id)
  if (!catIds.length) return []
  const { data } = await supabase
    .from('merchants')
    .select('slug, name_zh, name_en, category:categories(slug, name_zh, icon), district, google_rating, website')
    .eq('status', 'live')
    .in('category_id', catIds)
    .not('google_rating', 'is', null)
    .order('google_rating', { ascending: false })
    .limit(6)
  if (!data) return []
  return data.map((d: Record<string, unknown>) => ({
    ...d,
    category: Array.isArray(d.category) ? d.category[0] || null : d.category,
  })) as RelatedMerchant[]
}

export async function generateStaticParams() {
  const { data } = await supabase
    .from('insights')
    .select('slug')
    .eq('status', 'published')
  // Deduplicate slugs (multiple langs share same slug)
  const slugs = [...new Set((data || []).map(d => d.slug))]
  return slugs.map(slug => ({ slug }))
}

export async function generateMetadata({ params, searchParams }: PageProps): Promise<Metadata> {
  const { slug } = await params
  const { lang: langParam } = await searchParams
  const lang = parseLang(langParam)
  const lc = LANG_CONFIG[lang]

  const article = await getInsight(slug, lang)
  if (!article) return { title: UI_STRINGS[lang].notFound }

  const title = article.og_title || `${article.title} — CloudPipe AI`
  const description = article.og_description || article.description
  const availableLangs = await getAvailableLangs(slug)

  const alternates: Record<string, string> = {}
  for (const al of availableLangs) {
    const url = al === 'zh'
      ? `${siteUrl}/macao/insights/${slug}`
      : `${siteUrl}/macao/insights/${slug}?lang=${al}`
    alternates[LANG_CONFIG[al].hreflang] = url
  }

  return {
    title,
    description,
    openGraph: {
      title,
      description,
      type: 'article',
      locale: lc.locale,
      publishedTime: article.published_at || undefined,
      images: article.og_image ? [article.og_image] : undefined,
    },
    alternates: {
      canonical: lang === 'zh' ? `${siteUrl}/macao/insights/${slug}` : `${siteUrl}/macao/insights/${slug}?lang=${lang}`,
      languages: alternates,
    },
    other: { 'llms-txt': '/macao/llms-txt' },
  }
}


