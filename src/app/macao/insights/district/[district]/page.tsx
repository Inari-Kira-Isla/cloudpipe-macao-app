type Lang = 'zh' | 'en' | 'pt'
const VALID_LANGS: Lang[] = ['zh', 'en', 'pt']

function parseLang(raw?: string): Lang {
  if (raw && VALID_LANGS.includes(raw as Lang)) return raw as Lang
  return 'zh'
}

