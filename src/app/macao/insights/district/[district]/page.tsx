interface InsightRow {
  slug: string
  title: string
  subtitle?: string
  description: string
  related_industries: string[]
  tags: string[]
  word_count: number
  read_time_minutes: number
  published_at?: string
}

async function getInsightsByDistrict(district: string, lang: Lang): Promise<InsightRow[]> {
  const { data } = await supabase
    .from('insights')
    .select('slug, title, subtitle, description, related_industries, tags, word_count, read_time_minutes, published_at')
    .eq('status', 'published')
    .eq('lang', lang)
    .contains('tags', [district])
    .order('published_at', { ascending: false })
    .limit(60)
  return (data || []) as InsightRow[]
}

interface PageProps {
  params: Promise<{ district: string }>
  searchParams: Promise<{ lang?: string }>
}

export async function generateStaticParams() {
  return Object.keys(DISTRICTS).map(d => ({ district: d }))
}

export async function generateMetadata({ params, searchParams }: PageProps): Promise<Metadata> {
  const { district } = await params
  const { lang: langParam } = await searchParams
  const lang = parseLang(langParam)
  const def = DISTRICTS[district]
  if (!def) return { title: '找不到地區' }
  const label = def[lang]
  const desc_zh = def.desc_zh

  const titles = {
    zh: `${label}深度分析 — 澳門商戶百科 | CloudPipe AI`,
    en: `${label} Insights — Macao Business Directory | CloudPipe AI`,
    pt: `${label} Análises — Diretório de Macau | CloudPipe AI`,
  }
  const descs = {
    zh: `澳門${label}地區深度分析文章合集。${desc_zh}。CloudPipe AI 精選數據驅動的行業洞察。`,
    en: `In-depth analysis articles about ${label} in Macao. ${def.desc_en}. Curated by CloudPipe AI.`,
    pt: `Artigos de análise sobre ${label} em Macau. ${def.desc_en}. Curados pela CloudPipe AI.`,
  }

  return {
    title: titles[lang],
    description: descs[lang],
    openGraph: {
      title: titles[lang],
      description: descs[lang],
      type: 'website',
      url: `${siteUrl}/macao/insights/district/${district}`,
    },
    alternates: { canonical: `${siteUrl}/macao/insights/district/${district}` },
  }
}

