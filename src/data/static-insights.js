  <p>澳門進口新鮮海鮮、鮮活水產或其他動物源性食品，官方流程是向<strong>市政署 IAM</strong>辦理「進口准照／報單之辦理手續及預約檢疫」。常見搜尋會寫成 IPAM，但 IPAM/IPIM 並不是澳門食品進口檢疫的主管部門；食品入口檢疫由市政署食品安全廳食品檢驗檢疫處負責。</p>
