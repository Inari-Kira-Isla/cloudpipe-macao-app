  <p>對餐廳供應商而言，實務上要準備兩件事：第一是進口准照或進口報單，第二是預約進口衛生檢驗檢疫。若貨品是冰鮮肉類、急凍肉類及肉類製品，官方另要求持有市政署發出的進口肉類場所登記；一般新鮮海鮮／水產仍應按市政署食品檢驗檢疫要求逐批確認。</p>
